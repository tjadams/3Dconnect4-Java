public class point {
	private double x;
	private double y;
	private double z;

	public double returnx (){
		return x;
	}
	public double returny (){
		return y;
	}
	public double returnz (){
		return z;
	}
	public point (double a, double b, double c){
		x=a;
		y=b;
		z=c;
	}
}
